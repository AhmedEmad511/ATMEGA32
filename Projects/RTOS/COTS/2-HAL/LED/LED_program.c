#include "LED_interface.h"

u8 LED_TurnOn(LED_t* LED)
{
	DIO_u8SetPinDirection(LED ->PORT,LED->PIN,OUTPUT);
	DIO_u8SetPinValue(LED ->PORT,LED->PIN,HIGH);
}

u8 LED_TurnOff(LED_t* LED)
{
	DIO_u8SetPinDirection(LED ->PORT,LED->PIN,OUTPUT);
	DIO_u8SetPinValue(LED ->PORT,LED->PIN,LOW);

}
